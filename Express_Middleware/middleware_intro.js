/*
    *** Express.js Middleware ***
        => Middleware functions are functions that have
        access to the request object(req), the response object
        (res) and the next() in the application's req-res cycle

    *** Middleware function can perform the following tasks:
        => Execute any code
        => Make changes to the request and response object
        => End the request-response cycle
        => Call the next middleware by the next()
        => throw and catch errors

    *** There are different type of middleware we are used:
        => Application Level Middleware
        => Router Level Middleware
        => Error-Handling Middleware
        => Built-In Middleware
        => Third-Party Middleware

*/
